package com.lpu.EmployeeApp.dto;

public class EmployeeDTO {
	
	
	private String name;
	private long phone;
	private int age;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public EmployeeDTO() {
		super();
	}
	
	

}
